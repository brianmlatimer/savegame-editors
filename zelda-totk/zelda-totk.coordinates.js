function MapPin(index, icon, coordinates, map){
	this.index=index;

	this.icon=icon;
	this.coordinates=coordinates;
	this.map=map;
}
MapPin.prototype.clear=function(){
	if(!this.isFree()){
		this.icon=MapPin.ICON_NONE;
		this.coordinates={x:0.0, y:0.0};
		this.map=MapPin.MAP_MAIN;
		return true;
	}
	return false;
}
MapPin.prototype.isFree=function(){
	return this.icon===MapPin.ICON_NONE;
}
MapPin.prototype.save=function(){
	SavegameEditor.writeU32('MapData.IconData.StampData.Type', this.index, this.icon);
	SavegameEditor.writeVector2F('MapData.IconData.StampData.Pos', this.index, this.coordinates);
	SavegameEditor.writeU32('MapData.IconData.StampData.Layer', this.index, this.map);
}

MapPin.readAll=function(){
	var mapPins=[];
	var mapPinIcons=SavegameEditor.readU32Array('MapData.IconData.StampData.Type');
	for(var i=0; i<mapPinIcons.length; i++){
		mapPins.push(new MapPin(
			i,
			mapPinIcons[i],
			SavegameEditor.readVector2F('MapData.IconData.StampData.Pos', i),
			SavegameEditor.readU32Array('MapData.IconData.StampData.Layer', i)
		));
	}
	return mapPins;
}
MapPin.count=function(mapPins){
	var count=0;
	for(var i=0; i<mapPins.length; i++){
		if(!mapPins[i].isFree())
			count++;
	}
	return count;
}
MapPin.find=function(mapPins, x, y, z){
	for(var i=0; i<mapPins.length; i++){
		if(mapPins[i].coordinates.x===x && mapPins[i].coordinates.y===y && mapPins[i].map===MapPin.getMapByZ(z))
			return true;
	}
	return false;
}
MapPin.getMapByZ=function(z){
	if(z>750)
		return MapPin.MAP_SKY;
	else if(z<-300)
		return MapPin.MAP_MINUS;
	return MapPin.MAP_MAIN;
}

